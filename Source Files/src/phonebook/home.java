    private boolean checkCreditals(String userInput) {
        try {   
                BufferedReader br = new BufferedReader(new InputStreamReader(getClass().getResourceAsStream("input")));
                String str;
                String encrypedText="";
                String getKey="";
                int key;
                while((str=br.readLine())!=null) {
                    getKey="";
                    encrypedText="";
                    for(int i=0;str.charAt(i)!='+';i++) {
			getKey=getKey+str.charAt(i);
                    }
                    key=Integer.parseInt(getKey);
                    encrypedText=key+"+";
                    for(int i=0;i<userInput.length();i++) {
			encrypedText=encrypedText+(userInput.charAt(i)+key)+"+";
                    }
                    encrypedText=encrypedText+key;
                    if(str.equals(encrypedText))
			return true;
                }   
        }
        catch(Exception ex) {
               javax.swing.JOptionPane.showMessageDialog(null,"Passwords file not found","ERROR 404", javax.swing.JOptionPane.ERROR_MESSAGE);
               System.exit(0);
        }
        File fi=new File("phnoebook/input.txt");
        if(fi.exists())
        {
            boolean checkLocal=checkLocalCreditals(userInput);
            if(checkLocal==true)
                return true;
        }
        return false;
    }
