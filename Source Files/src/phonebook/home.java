    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        boolean status=checkCreditals(userName.getText()+"::"+password.getText());
        if(status==true) {
            try{
            File inputFile = new File("phnoebook/userdata/"+userName.getText());
	    File outputFile = new File("phnoebook/data");        
            outputFile.createNewFile();
            FileReader in = new FileReader(inputFile);
            FileWriter out = new FileWriter(outputFile);
            int c;
            while ((c = in.read()) != -1)
		out.write(c);
            in.close();
            out.close();                    
            }
            catch(Exception Ex) {
                  javax.swing.JOptionPane.showMessageDialog(null,"Unable to copy input data");
            }
            this.setVisible(false);
            Content frame=new Content(userName.getText());
            frame.setTitle("Phone Book");
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            
            
        }
        else {
     
            lpanel.setVisible(false);
            Error.setVisible(true);
        }
    }//GEN-LAST:event_loginActionPerformed

